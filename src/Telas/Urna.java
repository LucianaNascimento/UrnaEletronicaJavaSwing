    private void jBconfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconfirmaActionPerformed
          
           
      if (!jTFnumero1.getText().equals("") && !jTFnumero2.getText().equals("")){
          
        som("urna.wav");
        
        if(!"".equals(jTFnumero1.getText().trim()) && !"".equals(jTFnumero2.getText().trim())){
           
           int numCandidato = 0;
           if(jLnomeCandidato.getText().equals("Nulo")){
               numCandidato = 99;
            
                
           }else{
               numCandidato = Integer.parseInt(jTFnumero1.getText() + jTFnumero2.getText());
           }
           
        Candidato can;
        can = new Candidato();
        
        can.setNumero(numCandidato);
        
        CandidatoDAO bdc;
        bdc = new CandidatoDAO();
        
        
        bdc.computarVoto(can);
           
        }
        
        
        Eleitor ele;
        ele = new Eleitor();
        
        ele.setTitulo(VariavelGlobal.tituloEleitor);
         
        EleitorDAO bde;
        bde = new EleitorDAO();
        
        
        bde.computarVotoEleitor(ele);
        
         corrige();
         
         
                 
         TelaAutEleitor feleitor;
         feleitor = new TelaAutEleitor();
         feleitor.setVisible(true);
         
         dispose();
                
        
      }
    }//GEN-LAST:event_jBconfirmaActionPerformed

