    private void setarNumero(String numero){
        int numCandidato = 0;
        if(jTFnumero1.getText().equals("")){
           jTFnumero1.setText(numero);
        }
        else{
            if(jTFnumero2.getText().equals("")){
            jTFnumero2.setText(numero);
            
                numCandidato = Integer.parseInt(jTFnumero1.getText() + jTFnumero2.getText());
              
                    
                    CandidatoDAO bd;
                    bd = new CandidatoDAO();
        
                    Candidato can;
                    can = new Candidato();
                    
        
                    can = bd.consultaCandidatoPorNumero(numCandidato);
        
                    if(can != null){
                    
                    ImageIcon fotoCandidato;
                    fotoCandidato = new ImageIcon(getClass().getResource("../imagensCandidatos/" + can.getFoto() ));
                    
                    ImageIcon fotoVice;
                    fotoVice = new ImageIcon(getClass().getResource("../imagensCandidatos/" + can.getFotovice() ));
                                       
                    jLnomeCandidato.setText( can.getNome() );
                    jLvice.setText( can.getVice() );
                    jLpartidoCandidato.setText( can.getPartido() );
                    jLfotoCandidato.setIcon(fotoCandidato);
                    jLfotovice.setIcon(fotoVice);
            
                    
                    }else{
                        jLnomeCandidato.setText("Nulo");
                        jLvice.setText("Nulo");
                        jLpartidoCandidato.setText("Nulo");
                        
                        
                    }
        }

    } 
}

    
